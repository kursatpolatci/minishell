#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	size_t			slen;
	char			*dest;

	if (!s || !f)
		return (0);
	slen = ft_strlen(s);
	dest = malloc (sizeof(char) * slen + 1);
	if (!dest)
		return (0);
	i = 0;
	while (s[i])
	{
		dest[i] = (*f)(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
